#include <stdio.h>

char digits[10][3][4] = {
	{" _ ", "| |", "|_|"},
	{"   ", "  |", "  |"},
	{" _ ", " _|", "|_ "},
	{" _ ", " _|", " _|"},
	{"   ", "|_|", "  |"},
	{" _ ", "|_ ", " _|"},
	{" _ ", "|_ ", "|_|"},
	{" _ ", "  |", "  |"},
	{" _ ", "|_|", "|_|"},
	{" _ ", "|_|", " _|"}
};

int main()
{
	char str[] = "31415926535";
	for (int line = 0; line < 3; ++line) {
		for (char *p = str; *p != '\0'; ++p) {
			printf("%s", digits[*p - '0'][line]);
		}
		putchar('\n');
	}
	return 0;
}
