#include <stdio.h>

char digits[10][3][4] = {
	{" _ ", "| |", "|_|"},
	{"   ", "  |", "  |"},
	{" _ ", " _|", "|_ "},
	{" _ ", " _|", " _|"},
	{"   ", "|_|", "  |"},
	{" _ ", "|_ ", " _|"},
	{" _ ", "|_ ", "|_|"},
	{" _ ", "  |", "  |"},
	{" _ ", "|_|", "|_|"},
	{" _ ", "|_|", " _|"}
};

char letters[26][3][4] = {
	{" _ ", "|_|", "| |"},
	{"   ", "|_ ", "|_|"},
	{" _ ", "|  ", "|_ "},
	{"   ", " _|", "|_|"},
	{" _ ", "|_ ", "|_ "},
	{" _ ", "|_ ", "|  "},
	{" _ ", "|  ", "|_|"},
	{"   ", "|_|", "| |"},
	{"   ", " | ", " | "},
	{"   ", " | ", "_| "},
	{"   ", "|/ ", "|\\ "},
	{"   ", "|  ", "|_ "},
	{"   ", "|v|", "| |"},
	{"   ", "|\\|","|\\|"},
	{" _ ", "| |", "|_|"},
	{" _ ", "|_|", "|  "},
	{" _ ", "|_|", "  |"},
	{" _ ", "|_|", "| \\"},
	{" _ ", "|_ ", " _|"},
	{"___", " | ", " | "},
	{"   ", "| |", "|_|"},
	{"   ", "\\ /"," v "},
	{"   ", "| |", "|^|"},
	{"   ", "\\/ ","/\\ "},
	{"   ", "|_|", " _|"},
	{"__ ", " / ", "/_ "}
};

int main()
{
	char str[256];
	while (fgets(str, 256, stdin)) {
		for (int line = 0; line < 3; ++line) {
			for (char *p = str; *p != '\0'; ++p) {
				if ('0' <= *p && *p <= '9')
					printf("%s", digits[*p - '0'][line]);
				else if ('a' <= *p && *p <= 'z')
					printf("%s", letters[*p - 'a'][line]);
				else if ('A' <= *p && *p <= 'Z')
					printf("%s", letters[*p - 'A'][line]);
			}
			putchar('\n');
		}
	}
	return 0;
}
